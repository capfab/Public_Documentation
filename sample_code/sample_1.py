def mean_test(arg1, arg2):
    """This function calculates the mean of two arguments.

    :param arg1: argument 1
    :type arg1: int, float, ...
    :param arg2: argument 2
    :type arg2: int, float, ...
    :return: mean of arg1 and arg2
    :rtype: int, float, ...

    :Example:

        >>> mean_test(3, 5)
        4
    """
    return (arg1 + arg2) / 2
